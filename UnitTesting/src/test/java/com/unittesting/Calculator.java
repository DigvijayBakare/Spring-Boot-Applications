package com.unittesting;

public class Calculator {
    // sum of the numbers
    public int doSum(int a, int b, int c) {
        return a + b + c;
    }

    // product
    public int doProduct(int a, int b) {
        return a * b;
    }

    // compare two numbers
    public boolean compareNum(int a, int b) {
        return a == b;
    }
}
